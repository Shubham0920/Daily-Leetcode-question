class Solution{
public:
    int kPalindrome(string s, int n, int k)
    {
        // code here
        string t = s;
        reverse(t.begin(),t.end());
        // int n = s.size();
        vector<vector<int>> dp(n+1,vector<int>(n+1,0));
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                if(s[i-1] == t[j-1]){
                    dp[i][j] = 1+dp[i-1][j-1];
                }
                else{
                    dp[i][j] = max(dp[i][j-1],dp[i-1][j]);
                }
            }
        }
        int size = dp[n][n];
        return n-size <= k;
    }
};

