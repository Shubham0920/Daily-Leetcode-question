class Solution{
  public:
    TNode* solve(int i,int j,vector<int> &ans){
        if(i>j)return nullptr;
        
        int mid = (i+j)/2;
        if((j-i)%2==1) mid++;
        TNode* n = new TNode(ans[mid]);
        n->left = solve(i,mid-1,ans);
        n->right = solve(mid+1,j,ans);
        return n;
    }
    TNode* sortedListToBST(LNode *head) {
        //code here
        vector<int> ans;
        LNode* temp = head;
        while(temp!= nullptr){
            ans.push_back(temp->data);
            temp=temp->next;
        }
        
        int n = ans.size();
        return solve(0,n-1,ans);
    }
};

